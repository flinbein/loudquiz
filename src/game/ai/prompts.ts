export const ANSWER_CHECK_SYSTEM_PROMPT = `Ты — судья жестовой викторины "Loud Quiz". Проверяешь ответы игроков на вопрос-категорию.
Отвечай строго в формате JSON, без markdown-обёртки. Никаких пояснений вне JSON.`;

export interface AnswerCheckInput {
  questionText: string;
  playerAnswers: string[]; // one answer per player, in order
}

export function buildAnswerCheckPrompt(input: AnswerCheckInput): string {
  const players = input.playerAnswers.map((ans, i) => `- Игрок ${i + 1}: "${ans}"`).join("\n");
  return `Категория (вопрос жестовой викторины): "${input.questionText}"

Ответы игроков:
${players}

Правила проверки:
- Сгруппируй ответы по смыслу: синонимы, переводы, аббревиатуры, разные написания одного и того же — в ОДНУ группу
  Примеры одной группы: "Гелий" + "Helium" + "He" + "Гелий-4"; "США" + "Америка" + "United States"
- accepted: true, если ответ является корректным примером из категории (соответствует всем условиям формулировки)
- Из каждой группы засчитывается максимум один правильный ответ (уникальность)
- playerIds — список id игроков из этой группы
- note — опциональное пояснение (почему принято/отклонено, если неочевидно)
- commentary — краткий итог: сколько уникальных правильных ответов, что было интересного

Ответь только JSON в формате:
{
  "groups": [
    {
      "id": "g1",
      "accepted": true,
      "canonicalAnswer": "...",
      "playerIds": ["Игрок 1"],
      "note": "..."
    }
  ],
  "commentary": "..."
}`;
}

export function buildBlitzPrompt(input: BlitzInput): string {
  const itemsPerTask = input.itemsPerTask ?? 3;
  return `Сгенерируй ${input.count} заданий для блиц-раунда. Каждое задание — набор из ${itemsPerTask} слов/словосочетаний разной сложности, которые капитан объясняет жестами.

Входные данные:
\`\`\`json
{
  "count": ${input.count},
  "itemsPerTask": ${itemsPerTask}${
    input.pastTasks?.length
      ? `,\n  "pastTasks": ${JSON.stringify(input.pastTasks)}`
      : ""
  }
}
\`\`\`

Правила:
- Каждое задание содержит ровно ${itemsPerTask} вариантов с разными уровнями сложности
- Сложность 200 — легко (фрукт, животное, простое действие)
- Сложность 300 — умеренно (профессия, предмет быта, известная личность)
- Сложность 400 — сложно (абстрактное понятие, составное словосочетание)
- Не повторять слова из pastTasks
- text — это одновременно задание и ответ

Ответь только JSON в формате:
{
  "tasks": [
    {
      "id": "b1",
      "items": [
        { "text": "...", "difficulty": 200 },
        { "text": "...", "difficulty": 300 },
        { "text": "...", "difficulty": 400 }
      ]
    }
  ]
}`;
}
